#include "libft.h"
int	ft_atoi(const char *nptr)
{
	int	i;
	int	sign;

	i = 0;
	sign = 0;
	while (nptr[i] <= 48 && nptr[i] >= 57) // || nptr[i] != '\0')
	{
		if (nptr[i] == 45)
			sign++;
		i++;
	}
	return (i);
}
